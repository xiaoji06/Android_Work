package com.xiaoji.android_work.androidstudio;

public class Setting {

    /**
     * 代码提示大小写忽略 设置
     * File --> Settings -->Editor--> General ---> Code Completion -->Match case(不打勾)
     */

    /**
     * 快捷键eclipse化
     * File --> Settings -->Keymap --->Eclipse
     */

    /**
     * 软件界面字体大小
     * File --> Settings -->Appearance & Behavior --->Appearance -->Use custom font
     */

    /**
     * 修改编辑区域字体
     *  File --> Settings -->Editor-->Font
     */


}
